package Util;
public class Camera {
    public float x;
    public float y;
    public float z;
    public float xRot;
    public float yRot;
    public float zRot;
    public Camera() {
        this.x = 0.0f;
        this.y = 0.0f;
        this.z = 0.0f;
        this.xRot = 90.0f;
        this.yRot = 0.0f;
        this.zRot = 0.0f;
    }
}
